package ObjOrPro;

public class MathHelper {

    public static int min(int a, int b){
        if(a < b){
            return a;
        }
        else{
            return b;
        }
    }

    public static double min(double a, double b){
        if(a < b){
            return a;
        }
        else{
            return b;
        }
    }

}
